function parseRoom(raw) {
  if (!raw) return null;
  raw = raw.trim();
  if (raw.toLowerCase().includes('lunch')) return null;
  const lh = raw.match(/Lecture-hall-(\d+)_E2/i);
  if (lh) return { id: 'LH-' + lh[1], floor: lh[1][0], type: 'Lecture', label: 'Lecture Hall ' + lh[1] };
  const sw = raw.match(/S\/W-Lab-(\d+)[_-]?E?2?/i);
  if (sw) return { id: 'SW-' + sw[1], floor: sw[1][0], type: 'Lab', label: 'S/W Lab ' + sw[1] };
  const ss = raw.match(/Soft-skill-Lab-(\d+)_E2/i);
  if (ss) return { id: 'SS-' + ss[1], floor: ss[1][0], type: 'Lab', label: 'Soft Skill Lab ' + ss[1] };
  const ar = raw.match(/AR-VR-Lab-(\d+)_E2/i);
  if (ar) return { id: 'AR-' + ar[1], floor: ar[1][0], type: 'Lab', label: 'AR/VR Lab ' + ar[1] };
  const ui = raw.match(/UI\/UX-Lab-(\d+)_E2/i);
  if (ui) return { id: 'UI-' + ui[1], floor: ui[1][0], type: 'Lab', label: 'UI/UX Lab ' + ui[1] };
  const hw = raw.match(/H\/W-Lab-(\d+)_E2/i);
  if (hw) return { id: 'HW-' + hw[1], floor: hw[1][0], type: 'Lab', label: 'H/W Lab ' + hw[1] };
  const ph = raw.match(/Physics Lab-(\d+)_E2/i);
  if (ph) return { id: 'PH-' + ph[1], floor: ph[1][0], type: 'Lab', label: 'Physics Lab ' + ph[1] };
  return null;
}

