// Build structured schedule: { "Mo-1": Set of occupied rooms }
function buildOccupancyMap() {
  const map = {}; // key: "day-slot", value: Map of roomId -> {section, subject}
  RAW_SCHEDULE.forEach(([section, day, slot, roomStr, subject]) => {
    const room = parseRoom(roomStr);
    if (!room) return;
    const key = day + '-' + slot;
    if (!map[key]) map[key] = {};
    map[key][room.id] = { section, subject, room };
  });
  return map;
}

// All unique rooms across the whole timetable
function getAllRooms() {
  const rooms = {};
  RAW_SCHEDULE.forEach(([, , , roomStr]) => {
    const room = parseRoom(roomStr);
    if (room && room.id) rooms[room.id] = room;
  });
  return rooms;
}

